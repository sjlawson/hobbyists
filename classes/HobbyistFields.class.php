<?php

/**
 * The abstract class HobbyistFields allows model methods to iterate through a centralised, easily modified list
 * @author samuel
 *
 */
abstract class HobbyistFields {
	
	protected $id;
	
	protected $firstname;
	
	protected $lastname;
	
	protected $email;
	
	protected $sex; // accidental pun!
	
	protected $city;
	
	protected $state;
	
	protected $comments;
	
	protected $hobby_cycling;
	
	protected $hobby_frisbee;
	
	protected $hobby_skiing;
	
}